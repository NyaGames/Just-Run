package NyaGames.Just_Run;

public class Traps {
	//parametros de las trampas que pueden estar activas, con getters y setters
	public boolean I;
	public boolean O;
	public boolean P;
	
	Traps(){
		
	}

	public boolean getI() {
		return I;
	}

	public void setI(boolean i) {
		I = i;
	}

	public boolean getO() {
		return O;
	}

	public void setO(boolean o) {
		O = o;
	}

	public boolean getP() {
		return P;
	}

	public void setP(boolean p) {
		P = p;
	}
}
